  return icons[iconName] || icons.CubeIcon
}

// Status configuration
const statusConfig = computed(() => {
  const configs = {
    healthy: {
      label: 'Healthy',
      bgColor: 'bg-green-100 dark:bg-green-900/30',
      textColor: 'text-green-700 dark:text-green-400',
      dotColor: 'bg-green-500 dark:bg-green-400'
    },
    warning: {
      label: 'Warning',
      bgColor: 'bg-yellow-100 dark:bg-yellow-900/30',
      textColor: 'text-yellow-700 dark:text-yellow-400',
      dotColor: 'bg-yellow-500 dark:bg-yellow-400'
    },
    error: {
      label: 'Error',
      bgColor: 'bg-red-100 dark:bg-red-900/30',
      textColor: 'text-red-700 dark:text-red-400',
      dotColor: 'bg-red-500 dark:bg-red-400'
    },
    unknown: {
      label: 'Unknown',
      bgColor: 'bg-gray-100 dark:bg-gray-700',
      textColor: 'text-gray-700 dark:text-gray-400',
      dotColor: 'bg-gray-500 dark:bg-gray-400'
    }
  }
  return configs[props.status]
})

// Format resource count
const formattedCount = computed(() => {
  if (props.resourceCount >= 1000) {
    return `${(props.resourceCount / 1000).toFixed(1)}k`
  }
  return props.resourceCount.toString()
})

// Handle view click
function handleView(): void {
  emit('view', props.service)
}

// Handle configure click
function handleConfigure(): void {
  emit('configure', props.service)
}
